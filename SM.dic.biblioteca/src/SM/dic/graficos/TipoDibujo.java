package SM.dic.graficos;

/**
 * Tipo de forma que se va a dibujar
 * @author David Infante Casas
 */
public enum TipoDibujo {

    /**
     * No se dibuja ninguna forma
     */
    NULL,

    /**
     * Forma Punto
     */
    PUNTO,

    /**
     * Forma Línea
     */
    LINEA,

    /**
     * Forma Rectángulo
     */
    RECTANGULO,

    /**
     * Forma Elipse
     */
    ELIPSE,

    /**
     * Forma RectánguloRedondeado
     */
    REC_REDONDEADO,

    /**
     * Forma Arco
     */
    ARCO,

    /**
     * Forma Quad
     */
    QUAD,

    /**
     * Forma Cubic
     */
    CUBIC,

    /**
     * Forma TrazoLibre
     */
    TRAZO_LIBRE
}
